export const textColor=(isDark)=>{
    if(isDark) {
        return '#FFFF'
    }else {
        return '#000000'
    }
}
export const subTextColor=(isDark)=>{
    if(isDark) {
        return '#B9B9BA'
    }else{
        return 'rgb(50,50,50)'
    }
}
export const backgroundColor=(isDark)=>{
    if(isDark) {
        return '#222222'
    }else{
        return '#FFFF'
    }
}